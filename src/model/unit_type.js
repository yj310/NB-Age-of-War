class UnitType {
    constructor(
        image,
        mpCost,
        width,
        height,
        velocityX,
        velocityY,
        hp
    ) {
        this.image = image;
        this.mpCost = mpCost;
        this.width = width;
        this.height = height;
        this.velocityX = velocityX;
        this.velocityY = velocityY;
        this.hp = hp;
    }
}